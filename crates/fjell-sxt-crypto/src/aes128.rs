static RCON: [u8; 11] = [
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36,
];

fn xtime(x: u8) -> u8 {
    ((x << 1) ^ (if x & 0x80 != 0 { 0x1b } else { 0 }))
}

fn gmul2(x: u8) -> u8 { xtime(x) }
fn gmul3(x: u8) -> u8 { xtime(x) ^ x }

/// AES-128 key expansion.  Returns 11 round keys (each 16 bytes).
pub fn key_expand(key: &[u8; AES128_KEY_LEN]) -> [[u8; AES_BLOCK_LEN]; AES128_NR + 1] {
    let mut rk = [[0u8; AES_BLOCK_LEN]; AES128_NR + 1];
    rk[0].copy_from_slice(key);
    for i in 1..=AES128_NR {
        let prev = rk[i - 1];
        let mut w = [
            SBOX[prev[13] as usize] ^ RCON[i],
            SBOX[prev[14] as usize],
            SBOX[prev[15] as usize],
            SBOX[prev[12] as usize],
        ];
        for j in 0..16 {
            rk[i][j] = prev[j] ^ w[j % 4];
            if j % 4 == 3 && j != 15 {
                for k in 0..4 { w[k] = rk[i][j - 3 + k + 1 - 1 + 1]; }
                // simple: just XOR into previous word
                let base = j - 2;
                w[0] = rk[i][base];
                w[1] = rk[i][base + 1];
                w[2] = rk[i][base + 2];
                w[3] = rk[i][base + 3];
            }
        }
    }
    // Redo the key schedule more carefully
    let mut words = [[0u8; 4]; 44];
    for i in 0..4 {
        words[i] = [key[4*i], key[4*i+1], key[4*i+2], key[4*i+3]];
    }
    for i in 4..44 {
        let mut temp = words[i-1];
        if i % 4 == 0 {
            temp.rotate_left(1);
            for b in &mut temp { *b = SBOX[*b as usize]; }
            temp[0] ^= RCON[i/4];
        }
        for j in 0..4 { words[i][j] = words[i-4][j] ^ temp[j]; }
    }
    let mut rk2 = [[0u8; 16]; 11];
    for r in 0..11 {
        for j in 0..4 { rk2[r][4*j..4*j+4].copy_from_slice(&words[4*r+j]); }
    }
    rk2
}

fn sub_bytes(state: &mut [u8; 16]) {
    for b in state.iter_mut() { *b = SBOX[*b as usize]; }
}

fn shift_rows(s: &mut [u8; 16]) {
    // row 1: shift 1
    let t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
    // row 2: shift 2
    s.swap(2, 10); s.swap(6, 14);
    // row 3: shift 3
    let t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t;
}

fn mix_columns(s: &mut [u8; 16]) {
    for c in 0..4 {
        let i = 4 * c;
        let (s0, s1, s2, s3) = (s[i], s[i+1], s[i+2], s[i+3]);
        s[i]   = gmul2(s0) ^ gmul3(s1) ^ s2 ^ s3;
        s[i+1] = s0 ^ gmul2(s1) ^ gmul3(s2) ^ s3;
        s[i+2] = s0 ^ s1 ^ gmul2(s2) ^ gmul3(s3);
        s[i+3] = gmul3(s0) ^ s1 ^ s2 ^ gmul2(s3);
    }
}

fn add_round_key(state: &mut [u8; 16], rk: &[u8; 16]) {
    for i in 0..16 { state[i] ^= rk[i]; }
}

/// Encrypt a single 16-byte block with AES-128.
pub fn aes128_encrypt_block(block: &[u8; AES_BLOCK_LEN], key: &[u8; AES128_KEY_LEN])
    -> [u8; AES_BLOCK_LEN]
{
    let rk = key_expand(key);
    let mut state: [u8; 16] = *block;
    add_round_key(&mut state, &rk[0]);
    for r in 1..AES128_NR {
        sub_bytes(&mut state);
        shift_rows(&mut state);
        mix_columns(&mut state);
        add_round_key(&mut state, &rk[r]);
    }
    sub_bytes(&mut state);
    shift_rows(&mut state);
    add_round_key(&mut state, &rk[AES128_NR]);
    state
}
